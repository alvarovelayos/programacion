package com.alvaro.dam.tema1;

public class OperadoresRelacionales {

	public static void main(String[] args) {
		
		double op1,op2;
		char op3,op4;
		op1=1.34;
		op2=1.35;
		op3='a';
		op4='b';
		System.out.println("op1=" + op1 + "op2=" + op2);
		System.out.println("op1>op2 = " + (op1 > op2));
		System.out.println("op1<op2 =" + (op1 < op2));
		System.out.println("op1==op2 = " + (op1==op2));
		System.out.println("op1!=op2 = " + (op1!=op2));
		System.out.println("'a'>'b' = " + (op3>op4));
	}

}
